    public void atualizarListaProdutosVendidos(List<ProdutosDTO>tabelaProdutosDTO){
        String columns[] = {"ID","Produto","Valor","Status"};
        String dados[][] = new String[tabelaprodutos.size()][columns.length];

        int i = 0;
        for (ProdutosDTO p : tabelaprodutos) {
            dados[i] = new String[]{
                String.valueOf(p.getId()),
                p.getNome(),
                String.valueOf(p.getValor()),
                p.getStatus()};
            
                i++;
        }

        DefaultTableModel model = new DefaultTableModel(dados, columns);
        tabela.setModel(model);
    
    }
